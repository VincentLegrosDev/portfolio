const STORAGE_KEY = 'portfolio-language';

function getStoredLanguage() {
  try {
    return window.localStorage.getItem(STORAGE_KEY);
  } catch {
    return null;
  }
}

function setStoredLanguage(language) {
  try {
    window.localStorage.setItem(STORAGE_KEY, language);
  } catch {
    // Ignore storage errors and keep language only in memory.
  }
}

const defaultLanguage = document.documentElement.lang?.startsWith('fr') ? 'fr' : 'en';
let currentLanguage = getStoredLanguage() || defaultLanguage;

/* ============================================================
   NAVIGATION — scroll state & active link highlighting
   ============================================================ */
const navbar = document.getElementById('navbar');
const navLinks = document.querySelectorAll('.nav-link');
const navToggle = document.querySelector('.nav-toggle');
const navList = document.querySelector('.nav-links');
const langToggle = document.getElementById('lang-toggle');

// Scrolled state
window.addEventListener('scroll', () => {
  navbar.classList.toggle('scrolled', window.scrollY > 20);
  updateActiveLink();
}, { passive: true });

// Mobile toggle
navToggle.addEventListener('click', () => {
  const open = navList.classList.toggle('open');
  navToggle.classList.toggle('open', open);
  navToggle.setAttribute('aria-expanded', open);
});

// Close mobile nav on link click
navList.addEventListener('click', (e) => {
  if (e.target.classList.contains('nav-link')) {
    navList.classList.remove('open');
    navToggle.classList.remove('open');
    navToggle.setAttribute('aria-expanded', false);
  }
});

if (langToggle) {
  langToggle.addEventListener('click', (event) => {
    event.preventDefault();
    event.stopPropagation();
    const nextLanguage = currentLanguage === 'en' ? 'fr' : 'en';
    applyLanguage(nextLanguage);
  });
}

// Active link: highlight the section currently in view
function updateActiveLink() {
  const sections = document.querySelectorAll('section[id]');
  const scrollY = window.scrollY + window.innerHeight * 0.35;

  let current = '';
  sections.forEach((section) => {
    if (scrollY >= section.offsetTop) current = section.id;
  });

  navLinks.forEach((link) => {
    const href = link.getAttribute('href').replace('#', '');
    link.classList.toggle('active', href === current);
  });
}

/* ============================================================
   I18N
   ============================================================ */
function getNestedValue(obj, path) {
  return path.split('.').reduce((acc, key) => acc?.[key], obj);
}

function applyLanguage(language) {
  const dictionary = translations[language] || translations.en;
  currentLanguage = language;

  document.documentElement.lang = language;
  document.title = dictionary.pageTitle;

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    const value = getNestedValue(dictionary, key);
    if (typeof value === 'string') {
      element.textContent = value;
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    const value = getNestedValue(dictionary, key);
    if (typeof value === 'string') {
      element.setAttribute('placeholder', value);
    }
  });

  if (langToggle) {
    langToggle.textContent = dictionary.langToggleShort;
    langToggle.setAttribute('aria-label', dictionary.langToggleLabel);
    langToggle.setAttribute('aria-pressed', String(language === 'fr'));
  }

  setStoredLanguage(language);
}

/* ============================================================
   SMOOTH SCROLLING — anchor links with navbar offset
   ============================================================ */
document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
  anchor.addEventListener('click', (e) => {
    const targetId = anchor.getAttribute('href');
    if (targetId === '#') return;
    const target = document.querySelector(targetId);
    if (!target) return;
    e.preventDefault();

    const navHeight = parseInt(
      getComputedStyle(document.documentElement).getPropertyValue('--nav-height'),
      10
    ) || 64;

    const top = target.getBoundingClientRect().top + window.scrollY - navHeight;
    window.scrollTo({ top, behavior: 'smooth' });
  });
});

/* ============================================================
   SCROLL REVEAL — fade sections in on scroll
   ============================================================ */
const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.12, rootMargin: '0px 0px -40px 0px' }
);

document.querySelectorAll('.reveal').forEach((el) => revealObserver.observe(el));

/* ============================================================
   CONTACT FORM — simple client-side feedback
   ============================================================ */
const contactForm = document.getElementById('contact-form');
const formStatus = document.getElementById('form-status');

if (contactForm) {
  contactForm.addEventListener('submit', (e) => {
    e.preventDefault();

    const name = contactForm.name.value.trim();
    const email = contactForm.email.value.trim();
    const message = contactForm.message.value.trim();
    const copy = translations[currentLanguage]?.contact?.form || translations.en.contact.form;

    if (!name || !email || !message) {
      setStatus(copy.emptyError, true);
      return;
    }

    if (!isValidEmail(email)) {
      setStatus(copy.invalidEmail, true);
      return;
    }

    // Simulate async send
    const submitBtn = contactForm.querySelector('[type="submit"]');
    submitBtn.disabled = true;
    submitBtn.textContent = copy.sending;

    setTimeout(() => {
      submitBtn.disabled = false;
      submitBtn.textContent = copy.submit;
      contactForm.reset();
      setStatus(copy.success);

      setTimeout(() => setStatus(''), 5000);
    }, 1200);
  });
}

function setStatus(msg, isError = false) {
  formStatus.textContent = msg;
  formStatus.className = 'form-status' + (isError ? ' error' : '');
}

function isValidEmail(email) {
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
}

